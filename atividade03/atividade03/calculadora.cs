using System;
using System.Collections.Generic;
using System.Text;

namespace atividade03
{
    class Calculadora
    {
        public int A;
        public int B;
        public int Soma()
        {
            return A + B;
        }
        public int Sub()
        {
            return A - B;
        }
        public int Mult()
        {
            return A * B;
        }
        public int Div()
        {
            return A / B;
        }

    }
}
    
